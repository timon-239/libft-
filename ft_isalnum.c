int	ft_isalnum(int c)
{
	unsigned char	a;

	a = (unsigned char)c;
	if ((a >= '0' && a <= '9') || ((a >= 'A' && a <= 'Z') || (a >= 'a'
				&& a <= 'z')))
		return (1);
	return (0);
}
